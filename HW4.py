"""Даны два списка чисел. Посчитайте, сколько различных чисел содержится
одновременно как в первом списке, так и во втором.
"""

list1 = set('1 2 3 5 7'.split())
list2 = set('1 3 4 8 7'.split())
count = 0
for num1 in list1:
    for num2 in list2:
        if num1 == num2:
            count += 1
print(count)



"""Даны два списка чисел. Посчитайте, сколько различных чисел входит только
в один из этих списков"""

list1 = set('1 2 3 5 7 9'.split())
list2 = set('1 3 4 8 7 10'.split())
count = 0
for num1 in list1:
    for num2 in list2:
        if num1 == num2:
            count += 1
length = len(list1)
print(length - count)
